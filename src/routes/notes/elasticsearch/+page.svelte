<h2>Implementing a Custom Analyzer</h2>

<p>
	An analzyser processes the indexed data to be relevant to text searches and are made up of three
	parts: zero or more character filters, one tokenizer, and zero or more token filters. The
	character filter preprocess the stream of characters before passing the stream to the tokenizer.
	The tokenized stream is passed into a tokenizer.
</p>
