	<h2>Reading Notes</h2>

	<h3>Foreword / Preface</h3>
	<dl>
		<dt>pp. xx&ndash;xxi: What &ldquo;Pragmatic Programmer&rdquo; characteristics do you share?</dt>
		<dd>
			I&rsquo;d love to say that I have them all, but I need to qualify this.
			<dl>
				<dt>Early adopter / fast adapter</dt>
				<dd>
					I&rsquo;ve worn the hat of senior developer before, so I know there&rsquo;s value in
					carefully weighing changes. But I <em>love</em> adopting / adapting in my own side projects&mdash;I
					mean, does anyone ever stop tinkering with their own site?! For projects that will be inherited,
					sometimes company culture matters more than the &ldquo;best&rdquo; technology choice.
				</dd>

				<dt>Inqusitive</dt>
				<dd>
					I think I do this pretty well. If I&rsquo;m honest, I also feel fatigue at the exponential
					growth of frameworks. I&rsquo;m not fatigued learning about languages, nor when reading
					distilled wisdom (<cite>Prag Prog</cite>, <cite>Refactoring</cite>, etc.)
				</dd>

				<dt>Critical Thinker</dt>
				<dd>
					I think I learned this one much better with time. I tend towards idealism, so when someone
					pitches me one (<i>GraphQL is the future!</i>), I tend to try it on for size. Getting
					stung a few times by that idealism helps temper it with prudence.
				</dd>

				<dt>Realistic</dt>
				<dd>
					Again, improved with time. I&rsquo;m categorically bad at accurate estimating and tend to
					believe (only for myself) that I&rsquo;m not allowed to get stuck on stuff&mdash;when
					things that are supposed to be hard, are hard, I still get bummed out.
				</dd>

				<dt>Jack of all trades</dt>
				<dd>I sure try. I enjoy being <i>T</i>&dash;shaped.</dd>
			</dl>
		</dd>
	</dl>

