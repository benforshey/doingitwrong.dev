<h3>Clifton Strengths</h3>

<p>
	This test has changed names a few times, but I know it as the CliftonStrengths
	assessment. &ldquo;Strengths&rdquo; is in the name, so unsurprisingly it
	focuses on your strengths, highlighting your top five categories (or themes)
	from a list of thirty&dash;four possible categories. To summarize: know your
	strengths so that you can use them well.
</p>

<p>
	I remember being so struck by the assessment that I laminated my results; I
	wanted to grow into my strengths and shore up my weaknesses. Obviously, one
	needs to laminate that kind of knowledge. :-) My top five themes:
</p>

<ol>
	<li>Connectedness</li>
	<li>Learner</li>
	<li>Relator</li>
	<li>Intellection</li>
