<h2>Eponyms</h2>
<dl>
	<dt>Chesterton&rsquo;s Fence</dt>
	<dd>
		There exists in such a case a certain institution or law; let us say, for
		the sake of simplicity, a fence or gate erected across a road. The more
		modern type of reformer goes gaily up to it and says, &ldquo;I don&rsquo;t
		see the use of this; let us clear it away.&rdquo; To which the more
		intelligent type of reformer will do well to answer: &ldquo;If you don’t see
		the use of it, I certainly won’t let you clear it away. Go away and think.
		Then, when you can come back and tell me that you do see the use of it, I
		may allow you to destroy it.&rdquo;
	</dd>
</dl>

