<h1>What is Big O?</h1>

<ul>
	<li>way to categorize your algorithm&rsquo;s time or memory requirements</li>
	<li>not an exact measurement</li>
	<li>meant to generalize the growth of your algorithm</li>
</ul>

<h2>Why do we use it?</h2>

<p>
	It helps us make decisions about what data structures and algorithms to use.
	Knowing how they will perform can help you make the right decision between
	performance and complexity.
</p>
